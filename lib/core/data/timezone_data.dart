  // Search timezones by city, country, or timezone code
  static List<Map<String, String>> searchTimezones(String query) {
    if (query.isEmpty) return getPopularTimezones();

    final queryLower = query.toLowerCase();
    return timezones.where((tz) {
      final cityLower = tz['city']!.toLowerCase();
      final countryLower = tz['country']!.toLowerCase();
      final codeLower = tz['code']!.toLowerCase();

      return cityLower.contains(queryLower) ||
          countryLower.contains(queryLower) ||
          codeLower.contains(queryLower);
    }).toList();
  }

  // Get timezone by city name
  static Map<String, String>? getTimezoneByCity(String cityName) {
    try {
      return timezones.firstWhere(
        (tz) => tz['city']!.toLowerCase() == cityName.toLowerCase(),
      );
    } catch (e) {
      return null;
    }
  }
